# Mathematical operations

print(123 + 277) # This is an integer addition
print(123 - 277) # This is an integer subtraction
print(123 * 277) # This is an integer multiplication
print(123 / 277) # This is an integer division
print(123 // 277) # This is an integer floor division
print(123 % 277) # This is an integer modulo operation
print(123 ** 277) # This is an integer exponentiation
